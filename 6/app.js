
document.write(`Counting: <br>`)
for (var a = 0; a <= 15; a++) {
    document.write(`${a}, `)
    }

    
    document.write(`Reverse counting: <br>`)
    for (var b = 10; b <= 0 ; b--) {
        document.write(`${b}, `)
        }

        
        document.write(`Even: <br>`)
        for (var c = 0; c <= 20; c+2) {
            document.write(`${c}, `)
            }

            
            document.write(`Odd: <br>`)
            for (var d = 1; d <= 20; d+2) {
                document.write(`${d}, `)
                }
        
                
                document.write(`Series: <br>`)
                for (var e = 0; e <= 20; e+2) {
                    document.write(`${e}k, `)
                    }
